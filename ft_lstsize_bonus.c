#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		a;
	t_list	*p;

	p = lst;
	a = 0;
	if (!p)
		return (0);
	while (p)
	{
		p = (*p).next;
		a++;
	}
	return (a);
}
